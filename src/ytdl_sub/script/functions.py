class Functions:
    @staticmethod
    def lower(string: str) -> str:
        """
        Returns
        -------
        Lower-cased string
        """
        return string.lower()

    @staticmethod
    def upper(string: str) -> str:
        """
        Returns
        -------
        Upper-cased string
        """
        return string.upper()

    @staticmethod
    def capitalize(string: str) -> str:
        """
        Returns
        -------
        Capitalized string
        """
        return string.capitalize()
